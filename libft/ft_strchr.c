#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int i;

	i = 0;
	while (((char*)s)[++i - 1])
		if (((char*)s)[i - 1] == c)
			return (&((char*)s)[i - 1]);
	return ((((char*)s)[i - 1] == c) ? &((char*)s)[i - 1] : 0);
}
