#include "libft.h"

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	size_t	i;
	char	*sub;

	i = 0;
	if (!s)
		return (NULL);
	if (!(sub = ft_strnew(len)))
		return (NULL);
	return (ft_strncat(sub, (char*)(s + start), len));
}
